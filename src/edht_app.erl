-module(edht_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    io:format('stating supervisor~n'),
    File = os:getenv("CONFIG_FILE"),
    edht_sup:start_link([{config_file, File}]).

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

